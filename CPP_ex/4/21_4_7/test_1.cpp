//封装
//1、封装的意义
//将属性和行为作为一个整体，用来表现生活中的事物

//封装一个圆类，求圆的周长
//class代表设计一个类，后面跟着的是类名
#include<iostream>
#include<string>
using namespace std;
const double PI = 3.14;
class circle
{
public://访问权限  公共的权限

	//属性
    int M_R;
    //行为
	//获取到圆的周长
    double circleZC()
    {
        //2 * pi  * r
		//获取圆的周长
        return 2 * PI * M_R;
    }
};
int main()
{
    //通过圆类，创建圆的对象
	// c1就是一个具体的圆
    circle c1;
    c1.M_R = 10;//给圆对象的半径 进行赋值操作

	//2 * pi * 10 = = 62.8
    cout<<"圆的周长为："<<c1.circleZC()<<endl; 
    system("pause");
    return 0;
}